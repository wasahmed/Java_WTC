package com.vogella.javastarter.loop;

public class Loops {
    public static void main(String[] args) {

        for (int i=1; i < 10; i++){
            System.out.println("value of i : " + i);
        }

        int x = 1;

        while (x < 10) {
            System.out.println("value of x : " + x);
            x++;
        }




    }
}
